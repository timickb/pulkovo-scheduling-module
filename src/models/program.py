class StudyProgram:
    def __init__(self, id, name, discipline, features, hours, class_hours, practic_hours, sdo_hours, intramural_days, groups_amount, staff_amount, external_amount, teachers_amount, classrooms_amount):
        self.id = id
        self.name = name
        self.discipline = discipline
        self.features = features
        self.hours = hours
        self.class_hours = class_hours
        self.practic_hours = practic_hours
        self.sdo_hours = sdo_hours
        self.intramural_days = intramural_days
        self.groups_amount = groups_amount
        self.staff_amount = staff_amount
        self.external_amount = external_amount
        self.teachers_amount = teachers_amount
        self.classrooms_amount = classrooms_amount
